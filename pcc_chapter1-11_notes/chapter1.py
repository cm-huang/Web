# 终端

# 终端是一台电脑或者计算机系统（主机时代定义），用来让用户输入数据，及显示其计算结果的机器。
# 通俗来说，计算机的终端就是位于最外层的显示器、键盘或鼠标。
# 所以终端这个词在计算机系统里就是指最终的输入输出端，它是一个泛指。
# 终端有些是全电子的，也有些是机电的。终端其实就是一种输入输出设备，相对于计算机主机而言属于外设，本身并不提供运算处理功能。
# 如：终端会话窗口：cmd/powershell

# 终端机

# 终端机，即计算机显示终端，是计算机系统的输入、输出设备。
# 计算机显示终端伴随主机时代的集中处理模式而产生，并随着计算技术的发展而不断发展。
# 迄今为止，计算技术经历了主机时代、PC时代和网络计算时代这三个发展时期。
# 终端与计算技术发展的三个阶段相适应，应用也经历了字符哑终端、图形终端和网络终端这三个形态。
